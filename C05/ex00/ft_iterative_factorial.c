int	ft_iterative_factorial(int nb)
{
	int	i;
	int	result;

	result = 1;
	i = 0;
	if (nb < 0)
		return (0);
	while (i < nb)
	{
		result *= nb - i;
		i++;
	}
	return (result);
}
