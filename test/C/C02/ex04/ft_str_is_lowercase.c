int	ft_checker(char c)
{
	if (c >= 'a' && c<= 'z')
		return (1);
	return (0);
}

int	ft_str_is_lowercase(char *str)
{
	int	i;

	i = 0;
	if (str[0] == '\0')
		return (1);
	while (str[i] != '\0')
	{
		if(!(ft_checker(str[i])))
			return (0);
		i++;
	}
	return (1);
}
