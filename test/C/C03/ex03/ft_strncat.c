char	*ft_strncat(char *dest, char *src, unsigned int nb)
{
	unsigned int i;
	unsigned int j;

	i = 0;
	j = 0;
	while (dest[j] != '\0')
		j++;
	while (i < nb)
	{
		dest[j] = src[i];
		if (src[i] == '\0')
			return (dest);
		i++;
		j++;
	}
	dest[j] = '\0';
	return (dest);
}
